CREATE TABLE performance_metrics (
    metric_id INT PRIMARY KEY AUTO_INCREMENT,
    timestamp DATETIME,
    service_name VARCHAR(50),
    response_time_ms DECIMAL(10,2),
    api_latency_ms DECIMAL(10,2),
    error_rate_percent DECIMAL(5,2),
    uptime_percent DECIMAL(5,2),
    requests_count INT,
    active_users INT,
    cpu_utilization_percent DECIMAL(5,2),
    memory_utilization_percent DECIMAL(5,2)
);

CREATE TABLE incident_log (
    incident_id INT PRIMARY KEY AUTO_INCREMENT,
    incident_start DATETIME,
    incident_end DATETIME,
    service_affected VARCHAR(50),
    severity VARCHAR(20),
    description TEXT,
    root_cause TEXT,
    resolution TEXT,
    status VARCHAR(20)
);

CREATE TABLE sla_targets (
    service_name VARCHAR(50) PRIMARY KEY,
    max_response_time_ms DECIMAL(10,2),
    min_uptime_percent DECIMAL(5,2),
    max_error_rate_percent DECIMAL(5,2),
    max_api_latency_ms DECIMAL(10,2)
);

